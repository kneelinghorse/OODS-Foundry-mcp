/**
 * Escapes HTML special characters to prevent XSS injection.
 * Covers the five critical characters: &, <, >, ", '
 */
export function escapeHtml(value: string): string {
  return value
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}
